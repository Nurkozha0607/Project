# Slicing
b = "Hello, World!"
print(b[:5])

# Upper case
a = "Hello, World!"
print(a.upper())

# Lower case
a = "Hello, World!"
print(a.lower())

# Remove Whitespace
a = " Hello, World! "
print(a.strip()) # returns "Hello, World!"

# String Concatenation
a = "Hello"
b = "World"
c = a + " " + b
print(c)

# F-Strings
age = 36
txt = f"My name is John, I am {age}"
print(txt)

# Escape Character
txt = "We are the so-called \"Vikings\" from the north."